import { bookResult } from "@/shared/types";
import { neon } from "@neondatabase/serverless";
import { tokenProximitySpans, fuzzySpans } from "./censor";

const ERA_FILTERS = [
    'before:1970',
    'after:1970+before:1985',
    'after:1985+before:2000',
    'after:2000+before:2010',
    'after:2010+before:2018',
    'after:2018',
];

const CACHE_TTL = 1000 * 60 * 60 * 12; // 12 hours

const sql = neon(process.env.DATABASE_URL!);

let cache: Record<string, any> = {};

/**
 * Checks how long it has been since the last cache
 * @param timestamp 
 * @returns 
 */
function isCacheFresh(timestamp: number) {
    return Date.now() - timestamp < CACHE_TTL;
}

/**
 * Checks if the description is autogenerated by Google
 * @param description The description to check
 * @returns boolean - Whether the description is autogenerated
 */
function isBoilerplate(description: string) {
    return /^.{0,200} by .{0,100} released .{0,100} available now for purchase\.?$/i.test(
        description.trim(),
    );
}

/**
 * Looks for title/authors in description and censors
 * @param description The description to edit
 * @param phrases The phrases to censor
 * @returns The censored description
 */
function censorDescription(description: string, phrases: string[]) {
    const spans = [];
    const replacement = '█';

    // Finds matching spans
    for (const phrase of phrases) {
        const found = [...tokenProximitySpans(description, phrase), ...fuzzySpans(description, phrase)];

        spans.push(...found);
    }

    // Sort and merge overlapping spans
    spans.sort((a, b) => a.start - b.start); // Orders spans by where they appear left to right
    const merged = [];
    for (const span of spans) {
        const prev = merged[merged.length -1]; // The last span already added to merged
        if (prev && span.start <= prev.end) { // If prev overlaps with the current span, stretch prev to the end of the current span
            prev.end = Math.max(prev.end, span.end);
        } else {
            merged.push({...span}); // If there's no overlap, push the new span
        }
    }

    // Replace spans back-to-front so indexes stay valid
    let result = description;
    for (const {start, end} of merged.reverse()) {
        result = result.slice(0, start) + replacement.repeat(end - start) + result.slice(end);
    }

    return result
}

/**
 * Replaces all instances of the title and author from the description string
 * @param books The selected array of simplified book objects
 * @returns The selected array of simplified book objects, now with censored descriptions
 */
function censorBooks(books: bookResult[]) {
    for (let i = 0; i < books.length; i++) {
        const currentBook = books[i].volumeInfo;
        const { description } = currentBook;
        const phrases = [currentBook.title];

        if (currentBook.authors) {
            phrases.push(...currentBook.authors);
        }

        books[i].censoredDescription = censorDescription(description, phrases);
    }

    return books;
}

/**
 * Fetches books from the API
 * @param url The url to fetch
 * @returns resData - The results from the call
 */
export async function apiCall(url: string, signal?: AbortSignal, retries = 4): Promise<any> {
    const baseBackoff = 1200; // ms - increase base so we wait longer between attempts
    const maxJitter = 800;

    for (let attempt = 0; attempt <= retries; attempt++) {
        if (signal?.aborted) return null;

        try {
            const response = await fetch(url);
            const status = response.status;
            const textBody = await response.text().catch(() => "");
            let resData: any = null;
            try { resData = textBody ? JSON.parse(textBody) : null; } catch { resData = textBody; }

            if (response.ok) {
                return resData || null;
            }

            console.log(`[apiCall] attempt=${attempt} status=${status}`);

            // If service indicates when to retry, honor it
            const retryAfter = response.headers?.get?.("retry-after");
            if ((status === 429 || status === 503 || (status >= 500 && status < 600)) && attempt < retries) {
                let waitMs = baseBackoff * Math.pow(2, attempt) + Math.floor(Math.random() * maxJitter);
                if (retryAfter) {
                    const ra = Number(retryAfter);
                    if (!Number.isNaN(ra)) waitMs = Math.max(waitMs, ra * 1000);
                }
                console.log(`[apiCall] retrying after ${waitMs}ms (retry-after: ${retryAfter}) body:`, textBody);
                await new Promise((r) => setTimeout(r, waitMs));
                continue;
            }

            // non-retryable or exhausted retries: log full body and return null
            console.log("[apiCall] final response body:", textBody);
            return null;
        } catch (err) {
            if (err instanceof DOMException && err.name === 'AbortError') {
                return null;
            }

            // network/other error: back off and retry
            console.log(`[apiCall] network error attempt=${attempt}`, err);
            if (attempt < retries) {
                const waitMs = baseBackoff * Math.pow(2, attempt) + Math.floor(Math.random() * maxJitter);
                await new Promise((r) => setTimeout(r, waitMs));
                continue;
            }
            return null;
        }
    }

    return null;
}

/**
 * Uses Fisher-Yates to shuffle the results
 * @param selectedData - The book results to shuffle
 * @returns A shuffled array
 */
function shuffleBooks(selectedData: bookResult[]) {
    for (let i = selectedData.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [selectedData[i], selectedData[j]] = [selectedData[j], selectedData[i]];
    }
    return selectedData;
}

/**
 * Fetches a book by its ID
 * @param bookId The ID of the book to fetch
 * @param key The API key to use for the request
 * @returns The book result or null if not found
 */
export async function getBookById(bookId: string, key?: string): Promise<bookResult | null> {
    if (!key) {
        console.warn("[getBookById] Missing API_KEY");
        return null;
    }

    const res = await apiCall(
        `https://www.googleapis.com/books/v1/volumes/${bookId}?fields=id,volumeInfo/title,volumeInfo/authors,volumeInfo/description&key=${key}`,
    );
    return res || null;
}

/**
 * Fetches the books liked by a user
 * @param userId The user's ID to fetch liked books for
 * @returns An array of liked book results
 */
export async function getUserBooks(userId: string): Promise<bookResult[]> {
    if (!userId || userId === '') return [];

    const likedBooks = await sql`
        SELECT book_id, title, description, authors FROM public.liked_books WHERE user_id = ${userId}
    `;

    if (!likedBooks || likedBooks.length === 0) {
        return [];
    }

    const books = likedBooks.map((row) => {
        return {
            id: row.book_id,
            volumeInfo: {
                title: row.title,
                description: row.description,
                authors: row.authors ? row.authors.split(',').map((a: string) => a.trim()) : [],
            },
            censoredDescription: row.description,
        };
    });
    return books;
}

/**
 * Filters results by description quality, censors, and randomizes
 * @param selectedData 
 * @param userId - If provided, will filter out books the user has already liked
 * @returns 
 */
async function curateBooks(selectedData: bookResult[], userId?: string) {
    if (!selectedData || selectedData.length === 0) {
        return [];
    }

    const userBooks = await getUserBooks(userId || '');

    // Filter out results that are already in the userBooks list, if we have any
    const filteredForUser = userBooks.length > 0
        ? selectedData.filter(item => !userBooks.some(ub => ub.id === item.id))
        : selectedData;

    const filteredData =
        filteredForUser.filter(
            (item) =>
                !!item.volumeInfo?.description &&
                item.volumeInfo.description.length >= 100 &&
                !isBoilerplate(item.volumeInfo.description),
        ) || [];

    const censoredBooks = censorBooks(filteredData);
    const shuffledBooks = shuffleBooks(censoredBooks);

    return shuffledBooks;
}

async function getByEra(querystring: string, seen: Set<string>, pageSize: number, maxStartIndex: number, key: string, signal?: AbortSignal) {
    const results: bookResult[] = [];
    let consecutiveEraFailures = 0;
    for (const era of ERA_FILTERS) {
        if (signal?.aborted) break;

        const batch = await apiCall(
            `https://www.googleapis.com/books/v1/volumes?q=${querystring}+${era}&langRestrict=en&maxResults=${pageSize}&startIndex=0&fields=items(id,volumeInfo/title,volumeInfo/authors,volumeInfo/description)&key=${key}`, signal
        );

        if (!batch?.items?.length) {
            consecutiveEraFailures++;
            if (consecutiveEraFailures >= 2) {
                break;
            }
            continue;
        };

        consecutiveEraFailures = 0;

        for (const item of batch.items) {
            if (!seen.has(item.id)) {
                seen.add(item.id);
                results.push(item);
            }
        }

        if (batch.items.length < pageSize) continue;

        for (let i = pageSize; i < maxStartIndex; i += pageSize) {
            if (signal?.aborted) break;

            const nextBatch = await apiCall(
                `https://www.googleapis.com/books/v1/volumes?q=${querystring}+${era}&langRestrict=en&maxResults=${pageSize}&startIndex=${i}&fields=items(id,volumeInfo/title,volumeInfo/authors,volumeInfo/description)&key=${key}`, signal
            );
            if (!nextBatch?.items?.length) break;
            for (const item of nextBatch.items) {
                if (!seen.has(item.id)) { seen.add(item.id); results.push(item); }
            }
            if (nextBatch.items.length < pageSize) break;
        }
    }

    return results;
}

/**
 * Gets as many results as possible, up to 1000
 * @param querystring Either the subject or search query to look for
 * @returns 
 */
async function batchCallBooks(querystring: string, signal?: AbortSignal) {
    const key = process.env.API_KEY;
    if (!key) {
        console.warn("[batchCallBooks] Missing API_KEY");
        return [];
    }

    const seen = new Set<string>();
    const pageSize = 40;
    const maxStartIndex = 200;

    if (querystring.includes('memoir')) {
        const results: bookResult[] = [];
        for (let i = pageSize; i < maxStartIndex; i += pageSize) {
            if (signal?.aborted) break;

            const nextBatch = await apiCall(
                `https://www.googleapis.com/books/v1/volumes?q=${querystring}&langRestrict=en&maxResults=${pageSize}&startIndex=${i}&fields=items(id,volumeInfo/title,volumeInfo/authors,volumeInfo/description)&key=${key}`, signal
            );
            if (!nextBatch?.items?.length) break;
            for (const item of nextBatch.items) {
                if (!seen.has(item.id)) { seen.add(item.id); results.push(item); }
            }
            if (nextBatch.items.length < pageSize) break;
        }
        return results;
    } else {
        return await getByEra(querystring, seen, pageSize, maxStartIndex, key, signal);
    }
}

/**
 * Gets the book descriptions based on user-selected keywords
 * @param params The keyword to use for the search
 * @param q (optional) The user's custom query
 * @returns An array of books with censored descriptions
 */
export async function getBooks(
    params: string,
    q?: string,
    userId?: string,
    signal?: AbortSignal
) {
    if (signal?.aborted) return [];

    const paramsQuery =
        params && params !== "search" ? `subject:"${params}"` : null;
    const customQuery = params === "search" && q ? q : null;
    const querystring = customQuery || paramsQuery || '';

    console.log('[getBooks] cache', cache);

    const cached = cache[`books:${querystring}`];

    // Fresh cache — return immediately
    if (cached && isCacheFresh(cached.timestamp)) {
        return [...cached.finalResults].sort(() => Math.random() - 0.5);
    }

    // Background refresh helper
    const refresh = () =>
        batchCallBooks(querystring, signal)
            .then(selected => curateBooks(selected, userId))
            .then(results => {
                if (results.length > 0 && !signal?.aborted) {
                    cache[`books:${querystring}`] = { finalResults: results, timestamp: Date.now() };
                }
                return results;
            });

    // Stale cache — return it immediately, refresh in background
    if (cached) {
        console.log('Serving stale cache, refreshing in background');
        refresh(); // intentionally not awaited
        return [...cached.finalResults].sort(() => Math.random() - 0.5);
    }

    // No cache — user waits once
    return refresh();
}
